# ячейку с минимальным номером. Известно время, когда пассажиры сдают и забирают багаж (в минутах с начала суток).
# Ячейка доступна для багажа, начиная со следующей минуты, после окончания срока хранения. Если свободных ячеек
# не находится, то багаж не принимается в камеру хранения.
# Найдите количество багажа, которое будет сдано в камеры за 24 часа и номер ячейки, в которую сдаст багаж последний пассажир.
# Входные данные:
# В первой строке входного файла находится число K – количество ячеек в камере хранения,
# во второй строке файла число N – количество пассажиров, сдающих багаж (натуральное число, не превышающее 1000).
# Каждая из следующих N строк содержит два натуральных числа, не превышающих 1440: время сдачи багажа и время выдачи багажа.
# Выходные данные:
# Программа должна вывести два числа: количество сданных в камеру хранения багажа и номер ячейки,
# в которую примут багаж у последнего пассажира, который сможет сдать багаж.
