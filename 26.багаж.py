f = open('26дз.txt')
N = int(f.readline()) # пассажиры которые хотят сдать багаж
K = int(f.readline()) # свободные ячейки
data = [list(map(int, i.split())) for i in f] #split() разбивает по пробелам, map присеняет Int к каждому переданному значению.
# сюда кароч записывается время сдачи багажа а потом время его выдачи
data.sort()
chranilishe = [0]*K  #хранилище, тоесть сколько свобожных ячеек, изначально время 0 потому что в ячейке с самого начала нет багажа
count = last = 0 #количество и последняя ячейка
for i in range(N): # для каждого пассажриа анализируем скаждую ячейку
    for j in range(len(chranilishe)):
        if data[i][0] > chranilishe[j]: #data[i][0] означает что берём первый элемент(время сдачи багажа), потом сравниваем с
# с временем получения предыдущего багажа, чтобы понять свободна ли ячейка. тоесть если время сдачи багажа 10 а время выдачи предыдущего багажа
# 60 то багаж в ячейку не поместится и надо запихнуть его в следующую
            count+=1
            chranilishe[j] = data[i][1] # записываем, что в ячейке j лежит багаж, время сдачи воторого равняется второму числу в data
            last = j + 1
            break
print(count, last)

